
# isupper () Method :

'''
 Returns True if all cased characters in the string
 are uppercase and there is at least one cased character.
 Returns False otherwise.
'''

str1 = " LET'S WATCH CRICKET" # Full string in uppercase letter

str2 = 'let\'s play football' # Full string in lowercase letter

str3 = 'LET\'s GO FOR A WALK' # 1 letter in lowercase letter

print (str1.isupper())
print()  # 1 line space in o/p

print(str2.isupper())
print ()

print(str3.isupper())
