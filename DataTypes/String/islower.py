
# islower () Method :

'''
Returns True if all cased characters in the string are
lowercase and there is at least one cased character.
Returns False otherwise.

'''

str1 = " LET'S WATCH CRICKET" # Full string in uppercase letter

str2 = 'let\'s play football' # Full string in lowercase

str3 = 'let\'s go For a walk' # 1 letter in uppercase

print (str1.islower())
print()  # 1 line space in o/p

print(str2.islower())
print ()

print(str3.islower())
